function topKFrequent(nums, k) {
    let count = {};
    for (let num of nums) {
        if (count[num]) {
            count[num]++;
        } else {
            count[num] = 1;
        }
    }
    return Object.keys(count).sort((a, b) => count[b] - count[a]).slice(0, k);
}
